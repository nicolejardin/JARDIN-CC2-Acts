import java.math.*;

public class Circle {
  public static void main(String[] args) {
    
    
    for (int r = 1; r <= 5; r++)    {
      double pi = Math.PI;
      double area = r*r*pi;
      System.out.println("Value of r: " + r + " ");
      System.out.println("Area: " + area);
      
    }                                                                            
  }
}